# Mutual Information

## Continuous variables
"""

# ╔═╡ 426ccbc3-6393-4228-b38f-150748e52cbe
md"""
We can also use functions from the `CausalityTools` package. The `SurrogateTest` is a generic one-sided hypothesis test that checks whether $x$ and $y$ are independent (given $z$, if provided) based on resampling from a null distribution assumed to represent independence between the variables. The null distribution is generated by repeatedly shuffling the input data in some way that is intended to break any dependence between the input variables.

Let's take a look first at how to test independence with Pearson's Correlation using `SurrogateTest`:
"""

# ╔═╡ fbfc7a67-066b-47fd-a7e5-7e9cdcf6a768
independence(
	SurrogateTest(PearsonCorrelation()),
	tuzson.extension, tuzson.flexion
)

# ╔═╡ aebe621b-85f6-45a9-9586-28049322a77f
md"That give us the same result that we got before. Now let's calculate Shannon's mutual information (entropy). The higher is the mutual information, that indicates that variables are not independent."

# ╔═╡ fc09546b-082e-420e-ade3-8faa09576c1a
independence(
	SurrogateTest(TEShannon(), KSG1()),
	tuzson.extension, tuzson.flexion
)

# ╔═╡ 06eecf1a-b290-4304-89da-611391b58c62
md"""
!!! note

	`TEShannon` is suitted to look at time series. The KraskovStögbauerGrassberger1 mutual information estimator (you can use KSG1 for short) is the $I^{(1)}\kappa$-th nearest neighbor estimator.
"""

# ╔═╡ 324b0276-afcd-4c1f-9c9d-a1a7e0b304dc
md"""
## Categorical variables
"""

# ╔═╡ 7c0848cc-37ca-4d17-954c-caa3d46888f0
md"""
When our variables are categorical, instead of using the Pearson's correlation for the surrogate test, we can test for Shannon's Mutual Information (entropy) and add the `Contingency` function.

Let's go back to the comparison of question A3: Know how to comfort others with question A5: Make people at ease.
"""

# ╔═╡ a8b43e11-c51a-4c47-bd15-c592efc15100
independence(
	SurrogateTest(MIShannon(), Contingency()),
	bfi.a3, bfi.a5
)

# ╔═╡ a9ad68a9-0a32-4609-8d18-39e1da61fcf1
md"""
For the `attitude` example, the greatest Kendall correlation was between `rating` and `complaints`. Are those two variables independent?
"""

# ╔═╡ 2dd434ff-4386-4bfa-9e24-38768d54d629
independence(
	SurrogateTest(MIShannon(), Contingency()),
	att_mat[:,1], att_mat[:,2]
)

